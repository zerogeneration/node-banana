// Build a compiled, Turbopack-consumable snapshot of @zerogen/providers from the
// local zerogen-playground worktree, and pack it into vendor/zerogen-providers-0.1.0.tgz.
//
// WHY: node-banana runs on Turbopack (dev + build). Turbopack cannot consume the
// binding as live TypeScript source (it doesn't rewrite the NodeNext ".js"
// specifiers to ".ts"). Compiling to JS first sidesteps that entirely.
//
// USAGE:
//   node scripts/build-zerogen-binding.mjs   # then: npm install
//   ZEROGEN_PROVIDERS_DIR=/path/to/packages/providers node scripts/build-zerogen-binding.mjs
//
// TEMPORARY: remove this script, vendor/, and the file: dependency in package.json
// once @zerogen/providers is published / merged and consumed as a normal dependency.
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";
import { existsSync, mkdirSync, rmSync, writeFileSync, readFileSync } from "node:fs";

const NB_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const PROVIDERS_DIR =
  process.env.ZEROGEN_PROVIDERS_DIR ||
  "C:/Dev/zerogen-playground/.claude/worktrees/thirsty-almeida-0dd689/packages/providers";

const VENDOR_PKG = join(NB_ROOT, "vendor", "zerogen-providers");
const VENDOR_DIR = join(NB_ROOT, "vendor");
const TSC = join(NB_ROOT, "node_modules", "typescript", "bin", "tsc");
const TMP_TSCONFIG = join(PROVIDERS_DIR, "tsconfig.nbpack.json");

if (!existsSync(join(PROVIDERS_DIR, "src", "node-banana", "index.ts"))) {
  console.error(`[build-zerogen-binding] providers source not found at: ${PROVIDERS_DIR}`);
  console.error("Set ZEROGEN_PROVIDERS_DIR to the @zerogen/providers package directory.");
  process.exit(1);
}

console.log(`[build-zerogen-binding] source : ${PROVIDERS_DIR}`);
console.log(`[build-zerogen-binding] output : ${VENDOR_PKG}`);

rmSync(VENDOR_PKG, { recursive: true, force: true });
mkdirSync(VENDOR_PKG, { recursive: true });

// Emit JS + d.ts. NodeNext keeps the ".js" specifiers, which now resolve to the
// emitted ".js" files, so the compiled package is self-consistent plain JS.
writeFileSync(
  TMP_TSCONFIG,
  JSON.stringify(
    {
      extends: "../../tsconfig.base.json",
      compilerOptions: {
        rootDir: "src",
        outDir: VENDOR_PKG.replace(/\\/g, "/"),
        declaration: true,
        noEmit: false,
        skipLibCheck: true,
      },
      include: ["src/**/*.ts"],
      exclude: ["test/**/*.ts", "src/**/*.test.ts", "src/**/*.spec.ts"],
    },
    null,
    2,
  ),
);

try {
  execFileSync(process.execPath, [TSC, "-p", "tsconfig.nbpack.json"], {
    cwd: PROVIDERS_DIR,
    stdio: "inherit",
  });
} finally {
  rmSync(TMP_TSCONFIG, { force: true });
}

// Synthesize a package.json that points exports at the emitted JS and carries the
// adapters' SDK dependencies (so `npm install` of the tarball installs them).
const srcPkg = JSON.parse(readFileSync(join(PROVIDERS_DIR, "package.json"), "utf8"));
writeFileSync(
  join(VENDOR_PKG, "package.json"),
  JSON.stringify(
    {
      name: "@zerogen/providers",
      version: srcPkg.version ?? "0.1.0",
      private: true,
      type: "module",
      description:
        "Compiled snapshot of @zerogen/providers for node-banana. Generated by scripts/build-zerogen-binding.mjs. TEMPORARY — replace with the published/workspace dependency once the providers branch is merged.",
      exports: {
        ".": { types: "./index.d.ts", default: "./index.js" },
        "./node-banana": { types: "./node-banana/index.d.ts", default: "./node-banana/index.js" },
      },
      dependencies: srcPkg.dependencies ?? {},
    },
    null,
    2,
  ),
);

execFileSync(
  process.platform === "win32" ? "npm.cmd" : "npm",
  ["pack", VENDOR_PKG, "--pack-destination", VENDOR_DIR],
  { cwd: NB_ROOT, stdio: "inherit" },
);

console.log("\n[build-zerogen-binding] done. Now run: npm install");
